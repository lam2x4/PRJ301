package model;

/**
 *
 * @author ADMIN
 */
public class Order {
    int id;
    String date;
    int userId, totalmoney;

    public Order(int id, String date, int userId, int totalmoney) {
        this.id = id;
        this.date = date;
        this.userId = userId;
        this.totalmoney = totalmoney;
    }

    public Order() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getTotalmoney() {
        return totalmoney;
    }

    public void setTotalmoney(int totalmoney) {
        this.totalmoney = totalmoney;
    }
    
}
